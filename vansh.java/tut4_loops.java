public class tut4_loops {
    public static void main(String[] args) {
        // for loop
        for (int j = 0; j < 4; j++) {
            System.out.println("hello! by for loop");
        }
        // while loop
        int i = 0;
        while (i < 4) {
            System.out.println("hello! by while loop");
            i += 1;
        }
        // do while loop
        i = 0;
        do {
            System.out.println("hello! by do while");
            i += 1;
        } while (i < 4);

    }
}
